package mhel.itu.bachelor.shortestpathmap.algorithm;

public class BoundsDTO {
    public float maxX;
    public float minX;
    public float maxY;
    public float minY;

    public BoundsDTO(float maxX, float minX, float maxY, float minY) {
        this.maxX = maxX;
        this.minX = minX;
        this.maxY = maxY;
        this.minY = minY;
    }
}
